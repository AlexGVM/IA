package org.example;

public class Calculadora {

    public double Sumar(double a ,double b){
        return a+b;
    }
    public double Restar(double a ,double b){
        return a-b;
    }
    public double Multiplicar(double a ,double b){
        return a*b;
    }
    public double dividir(double a ,double b){
        return a/b;
    }
}
